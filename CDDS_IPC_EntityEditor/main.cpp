#include "raylib.h"
#include "EntityEditorApp.h"

int main(int argc, char* argv[])
{
    float deltaTime = 0;
    EntityEditorApp app(800, 450);
    // Initialization
    //--------------------------------------------------------------------------------------
    app.Startup();
    //--------------------------------------------------------------------------------------

    // Main game loop
    while (!WindowShouldClose())    // Detect window close button or ESC key
    {
        deltaTime = GetFrameTime();

        // Update
        //----------------------------------------------------------------------------------
        app.Update(deltaTime);
        //----------------------------------------------------------------------------------

        // Draw
        //----------------------------------------------------------------------------------
        app.Draw();
        //----------------------------------------------------------------------------------
    }

    // De-Initialization
    //--------------------------------------------------------------------------------------   
    app.Shutdown();
    //--------------------------------------------------------------------------------------

    return 0;
}